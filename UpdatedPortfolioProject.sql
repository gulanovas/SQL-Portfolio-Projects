-- Identify the dates with zero new COVID-19 cases.

SELECT location, COUNT(date) AS DaysWithZeroCases
FROM PortfolioProject..CovidDeaths
WHERE new_cases = 0 AND continent IS NOT NULL
GROUP BY location
ORDER BY location



-- Average Daily Cases Across All Countries

WITH DailyAvg (date, AverageDailyCases) AS
(
SELECT date, AVG(new_cases) AS AverageDailyCases
FROM PortfolioProject..CovidDeaths
WHERE continent IS NOT NULL
GROUP BY date
)
SELECT 'World Wide' AS Place, Date, AverageDailyCases
FROM DailyAvg
WHERE AverageDailyCases IS NOT NULL
ORDER BY date;



